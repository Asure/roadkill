** INITSND
**
** JFL 20 NOV 92
** JFL 01 FEB 92; SNDARENAX
**
INITSND
RESTORESNDMAP
	MOVI	ROMSOUNDTBL,A0
	MOVI	ARENASOUNDTBL,A1
	MOVI	SNDTSIZE*SNDARENAX/32,A2
ISLOOP
	MOVL	*A0+,*A1+
	DSJ	A2,ISLOOP
	RETS

**
** SNDREMAP
**
** IN
**   A2 SNDREMAPDATA
**
** JFL 10 DEC 92
**
SNDREMAP
	MMTM	SP,A0,A1,A4
	MOVI	ARENASOUNDTBL,A0
SRLOOP
	;GET SRC & REPEAT FLAG
	MOVW	*A2+,A1
	JRZ	SRX
	JRP	SRNORUN
	SLL	16+1,A1
	SRL	16+1,A1
	CLR	A4			;SRC ADD
	MOVW	*A2+,A3	   		;COUNT
	JRP	SRREMAP
	MOVK	SNDTSIZE,A4		;SRC ADD
	JRUC	SRREMAP

SRNORUN
	MOVK	1,A3			;COUNT
	CLR	A4			;SRC ADD

SRREMAP
	;ADR OF TBL TO MODIFY
	SLL	SNDTSIZEOF,A1
	ADD	A0,A1
	
SRRMLOOP
	MOVL	*A2,*A1
	ADDK	32,A1
	ADD	A4,A2
	DSJ	A3,SRRMLOOP
	JRUC	SRLOOP
SRX
	MMFM	SP,A0,A1,A4
	RETS

**
** MAKESND
**
** MAKE FROM RAM OR ROM TBL
**
** IN
**   A14 SNDID
**
** JFL 20 NOV 92
** JFL 01 FEB 93
**
MAKESND
	MMTM	SP,A0

	;RAM OR ROM TBL
	CMPI	SNDARENAX,A14
	JRHS	MSROM

	SLL	5,A14		;SIZEOF SOUNDTBL
	ADDI	ARENASOUNDTBL,A14
	.IF DEBUG
	CMPI	ARENASOUNDTBLX,A14
	TLOCKON	HS
	.ENDIF ;DEBUG
	JRUC	MSUSE

MSROM
	SLL	5,A14		;SIZEOF SOUNDTBL
	ADDI	ROMSOUNDTBL,A14
	.IF DEBUG
	CMPI	ROMSOUNDTBLX,A14
	TLOCKON	HS
	.ENDIF ;DEBUG
MSUSE
	MOVL	*A14,A0
	JRZ	MSX
	CALLA	ONESND ;IN:A0
MSX
	MMFM	SP,A0
	RETS

**
** ARENASND
**
** FROM RAM TBL
**
** IN
**   A14 SNDID
**
** JFL 20 NOV 92
** JFL 01 FEB 93
**
ARENASND
	MMTM	SP,A0

	SLL	5,A14		;SIZEOF SOUNDTBL
	ADDI	ARENASOUNDTBL,A14
	.IF DEBUG
	CMPI	ARENASOUNDTBLX,A14
	TLOCKON	HS
	.ENDIF ;DEBUG
	MOVL	*A14,A0
	JRZ	ASX

	CALLA	ONESND ;IN:A0
ASX
	MMFM	SP,A0
	RETS

